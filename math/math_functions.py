import math # this how to import modules, making math an object in this file
x = 2.6 
print(round(x)) # round function will round up .6, and round down .5

print(abs(-2.6)) # this is absolute function, always return the positive value

print(math.ceil(2.9)) # this is example of using . operator on math object to access it's methods
print(math.floor(2.9))

